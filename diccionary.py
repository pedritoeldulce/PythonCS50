# The dictionaries are used to store data values in key_values pairs


my_dict = {
    "paolo":31,
    "Noa": 3
}

# show diccionary
print(my_dict)

# show key:value 
print(my_dict["paolo"])

# Add or change the key:value pair
my_dict["paolo"]=30
print(my_dict)

my_dict["Filomena"]=61
print(my_dict)